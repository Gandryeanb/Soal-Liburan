function britaniaFlag(param) {
  var res = []
  var subRes = []
  var a = 0
  var b = param -1
  
  if (param % 2 === 0) {
    return "invalid input"
  } else {
    for (var i = 0; i < param; i++) {
      for (var j = 0; j < param; j++) {
        if (a === j || b === j || j === Math.floor(param/2) || i ===  Math.floor(param/2)) {
          subRes.push("*")
        } else {
          subRes.push(" ")
        }
      }
      res.push(subRes)
      subRes = []
      a++
      b--
    }
  }
  return res
}

